"""
Programacao Orientada a Objetos - POO

- POO é um paradigma de programacao que utiliza que mapeamentos
de objetos no mundo real para modelos computacionais.

- Paradigma de programacao é a forma/ metodologia utilizada para
 pensar/ desenvolver sistemas.

Principais elementos da Orientacao a Objetos
- Classe -> Modelo do objeto do mundo real sendo representado computacionalmente;
- Atributo -> Caracteristicas do objeto
- Método -> Comportamentos do objeto (funcoes)
- Construtor -> Método especial utilizado para criar objetos
- objetos -> Instancia da classe

"""

numero = 10

print(numero)
print(type(numero))

nome = 'Geek'
print(nome)
print(type(nome))

class Produto:
    pass

ps4 = Produto()
print(ps4)
print(type(ps4))